"""
Tests for the ingestion pipeline.

Two layers covered:
  - Error handling (no model loaded — fast tests, no slow marker)
  - Integration end-to-end: ingestion → vectorstore → embeddings → retrieval
    (real model loaded — marked @pytest.mark.slow)

Isolation strategy:
  - Each test gets its own ChromaDB directory via the `tmp_path` fixture.
  - settings.chroma_path is monkeypatched to point at it.
  - get_vectorstore.cache_clear() is called so the cached singleton is rebuilt
    against the new path.
  - The embeddings model is NOT cleared between tests — it's shared via
    @lru_cache to amortize its 30-60s load cost across the whole suite.

The PDF fixture (tests/fixtures/sample.pdf) was generated by
_generate_sample.py in the same folder. See that script if it ever needs
to be regenerated.
"""
from pathlib import Path

import pytest

from app.core import vectorstore as vectorstore_module
from app.core.config import settings
from app.core.exceptions import IngestionError
from app.core.ingestion import IngestionResult, ingest_pdf, ingest_text

PDF_FIXTURE = Path(__file__).parent / "fixtures" / "sample.pdf"


@pytest.fixture
def isolated_vectorstore(tmp_path, monkeypatch):
    """
    Point the vector store at a per-test temporary directory.

    Without this, every test would write to the real ./chroma_db, polluting
    each other and leaving residue on disk. With this, each test starts from
    an empty database and pytest cleans up automatically.

    We must also clear the @lru_cache on get_vectorstore — otherwise the
    factory would return the singleton built on the previous path.
    """
    monkeypatch.setattr(settings, "chroma_path", str(tmp_path / "chroma_db"))
    vectorstore_module.get_vectorstore.cache_clear()
    yield
    # Post-test cleanup: also clear cache so the next test rebuilds cleanly.
    vectorstore_module.get_vectorstore.cache_clear()


# ---------------------------------------------------------------------------
# Error handling tests — no model loaded, fast.
# ---------------------------------------------------------------------------


def test_ingest_text_raises_on_empty_string():
    """Empty text must raise IngestionError with a clear message."""
    with pytest.raises(IngestionError, match="empty"):
        ingest_text("", "empty.txt")


def test_ingest_text_raises_on_whitespace_only():
    """Whitespace-only text must be treated as empty."""
    with pytest.raises(IngestionError, match="empty"):
        ingest_text("   \n\t  ", "blank.txt")


def test_ingest_pdf_raises_on_missing_file():
    """A nonexistent file path must raise IngestionError."""
    with pytest.raises(IngestionError, match="not found"):
        ingest_pdf("/nonexistent/path/to/file.pdf")


